let direction = prompt("Enter direction")
if (direction == "up") {
  console.log("Go up");
} else if (direction == "right") {
  console.log("Go right");
} else if (direction == "left") {
  console.log("Go left");
} else if (direction == "down") {
  console.log("Go down");
} else {
  console.log("Don't go");
}